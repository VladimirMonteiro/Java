package org.example.atividades2;

/*Calcule a média de 3 notas de um aluno.*/
public class Atv3 {
    public static void main(String[] args) {
        double nota1 = 7.5;
        double nota2 = 8.0;
        double nota3 = 9.0;

        System.out.println("A média das notas é: " + (nota1 + nota2 + nota3) / 3);
    }
}
